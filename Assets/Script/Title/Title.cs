using UnityEngine;
using UnityEngine.SceneManagement;


//-------------------------------------------------------------------------------------------------
// 
//-------------------------------------------------------------------------------------------------
public class Title : MonoBehaviour
{
    //-------------------------------------------------------------------------------------------------
    // 変数宣言。
    //-------------------------------------------------------------------------------------------------
    [Header("的")]
    [SerializeField] private GameObject[] _obj;

    //-------------------------------------------------------------------------------------------------
    // private void Awake()
    //-------------------------------------------------------------------------------------------------
    private void Awake()
    {
        
    }


    //-------------------------------------------------------------------------------------------------
    // private void Start()
    //-------------------------------------------------------------------------------------------------
    private void Start()
    {
        
    }


    //-------------------------------------------------------------------------------------------------
    // private void Update()
    //-------------------------------------------------------------------------------------------------
    private void Update()
    {
        if ( !_obj[0].activeSelf )
        {
            SceneManager.LoadScene("Stage01Scene");
        }
        else if (!_obj[1].activeSelf)
        {
            SceneManager.LoadScene("Stage02Scene");
        }
        else if (!_obj[2].activeSelf)
        {
            SceneManager.LoadScene("Stage03Scene");
        }
        else if (!_obj[3].activeSelf)
        {
            SceneManager.LoadScene("Stage04Scene");
        }
        else if (!_obj[4].activeSelf)
        {
            SceneManager.LoadScene("Stage05Scene");
        }
    }
}